import numpy as np


# 1D array
array = np.array([1, 2, 3, 4, 5])  # array([])
print(f"The 1D array=\n{array}")


# # 2D array

array_2d = np.array([[1, 2, 3, 5, 6], [0, 9, 8, 7, 6]])
print(f"The 2D array = \n{array_2d}")


# # 3D array
array_3d = np.array([[6, 2, 43], [7, 8, 4], [8, 23, 56]])  # [[1,2,3],[]]

print(f"The 3D array=\n{array_3d}\n")
